C
    1 IF(CHAR(1).GT.0) RETURN
      NAME(1)=CHAR(1)
      GO TO 7
C
C     LEFT JUSTIFIED SEVEN BIT ASCII CHARACTERS ARE EXTRACTED BY
C     MASKING AND SHIFTING
C
    2 NAME(1)=(NAME(1).AND."774000000000).OR.((CHAR(2)/128)
     1.AND."003777777776)
