C
	DIMENSION X(300)
C
C   INPUT RAW TEST SCORES
	DO 1 I=1,300
	WRITE(5,6)
	READ(5,7)X(I)
	IF(X(I).LE.0.0)GO TO 2
	SUM=SUM+1.0
	ISUM=ISUM+1
	TOT1=TOT1+X(I)
	TOT2=TOT2+(X(I))**2.0
1	CONTINUE
C
C   CALCULATE SAMPLE MEAN
2	AVG=TOT1/SUM
C
C   CALCULATE SAMPLE STANDARD DEVIATION
	STDEV=SQRT((TOT2-(TOT1**2/SUM))/SUM)
C
C   BEGIN SORT
	N1=ISUM-1
	DO 4 I=1,N1
	J=I+1
	DO 3 K=J,ISUM
	IF(X(I).GE.X(K))GO TO 3
	TEMP=X(I)
	X(I)=X(K)
	X(K)=TEMP
3	CONTINUE
4	CONTINUE
C
C   INPUT TOTAL POPULATION PARAMETERS
	WRITE(5,8)AVG,STDEV
	WRITE(5,9)
	READ(5,7)POAVG
	WRITE(5,10)
	READ(5,7)PODEV
C
C   PRINT REPORT ON TERMINAL
	WRITE (5,12)POAVG,PODEV
	DO 5 I=1,ISUM
	Z=(X(I)-AVG)/STDEV
	ADJVU=POAVG+Z*PODEV
	WRITE(5,11)X(I),ADJVU
5	CONTINUE
	STOP'      END OF REPORT'
C
6	FORMAT(14H TEST SCORE ? ,$)
7	FORMAT(F4.0)
8	FORMAT(19H THIS SAMPLE HAS A ,F6.2,
	110H MEAN WITH/3H A ,F6.2,29H
	2 STANDARD DEVIATION. TO PRINT /
	339H THE NORMALIZED VALUES FOR THIS
	4 SAMPLE,)
9	FORMAT(28H WHAT MEAN SHOULD BE USED ? ,$)
10	FORMAT(27H+WHAT STANDARD DEVIATION ? ,$)
11	FORMAT(1H 5XF4.0,5X,F6.3)
12	FORMAT(1H1,3X,21H   POPULATION MEAN =
	1 F6.2/1H 3X,21HSTANDARD DEVIATION = F6.2/
	21H0,6X,3HRAW,3X,10HNORMALIZED/
	31H ,4X,6HSCORES,4X,6HSCORES/
	41H ,4X,6H------,2X,10H----------)
	END
